function solution(cards1, cards2, goal) {
    let stack = [];

    for(let i=0; i<goal.length;i++){
        if(goal[i] === cards1[0]){
            stack.push(cards1.shift())
        }
        else if(goal[i] === cards2[0]){
            stack.push(cards2.shift())
        }
        else return "No"

    }
    
    if(stack.toString() == goal)
        return "Yes"
}